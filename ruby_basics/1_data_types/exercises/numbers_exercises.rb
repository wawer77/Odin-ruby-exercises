def add(a, b)
  # return the result of adding a and b
  a + b
end

def subtract(a, b)
  a -b
end

def multiply(a, b)
  a * b
end

def divide(a, b)
  a / b
end

def remainder(a, b)
  a % b
end

def float_division(a, b)
  a.to_f / b
end

def string_to_number(string)
  string.to_i
end

def even?(number)
  number.even?
end

def odd?(number)
  number.odd?
end
